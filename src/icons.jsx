// Utility function to get status icon based on status text
export const getStatusIcon = (status, size = "1em", className = "") => {
  const statusLower = status.toLowerCase();
  
  if (statusLower.includes('on time')) {
    return <OnTimeIcon size={size} className={className} />;
  } else if (statusLower.includes('early')) {
    return <EarlyIcon size={size} className={className} />;
  } else if (statusLower.includes('late')) {
    return <LateIcon size={size} className={className} />;
  } else if (statusLower.includes('cancelled')) {
    return <CancelledIcon size={size} className={className} />;
  } else if (statusLower.includes('starts here')) {
    return <StartsHereIcon size={size} className={className} />;
  } else {
    return <InfoIcon size={size} className={className} />;
  }
};

